// Write a js code for generating random number between 1 to 10

function randomOp(){
    console.log(Math.ceil(Math.random()*10));
}

randomOp();

// Write a js code for generating random number between 0 to 10
function random0to10(){
    console.log(Math.floor(Math.random()*11));
}

random0to10();

